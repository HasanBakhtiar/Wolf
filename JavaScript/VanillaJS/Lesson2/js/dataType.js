// Basic Data Type - Bəsit Data Tipi
// 1String - mətin data tipi
const infoData1 = "Hello JS"; 
const infoData2 = "51"; 
console.log(infoData1+infoData2);


// 2Number - rəqəm data tipi
const infoNumber1  =  5;
const infoNumber2  =  15;
console.log(infoNumber1+ infoNumber2);


// 3Boolean - məntiqi data tipi
const infoBoolean1  = true;
const infoBoolean2  = false;
console.log(infoBoolean1);
console.log(infoBoolean2);


// Composite Data Type - Birləşik Data Tipi
// 1Array - bir və birdən çox məlumatları eni yerdə saxlamaq üçün nəzərdə tutlur
// const infoArray1  = new Array();
